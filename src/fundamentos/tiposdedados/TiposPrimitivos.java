package fundamentos.tiposdedados;

public class TiposPrimitivos {
    public static void main(String[] args) {
        // Todos os tipos primitivos são numéricos
        // int, double, float, char, short, long, byte, boolean

        int numeroInteiro = 5; // long, short e byte também são inteiros
        double numeroRealPrecisaoDupla = 5.67;
        float numeroReal = 5.5f;

        char caractere = 'K';

        boolean verdadeiro = true;
        boolean falso = false;

        // Obs: Em Java, string não é um tipo primitivo, é uma classe
    }
}
